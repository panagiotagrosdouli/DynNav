#!/usr/bin/env python3
"""Statistical analysis for SelfAwareAStar randomized benchmark outputs.

The script reads the CSV generated by randomized_ablation_benchmark.py and
produces a Markdown report with bootstrap confidence intervals and effect sizes.

Run from repository root:

    python benchmarks/self_aware_astar/randomized_ablation_benchmark.py --seeds 50
    python benchmarks/self_aware_astar/statistical_analysis.py
"""

from __future__ import annotations

import argparse
import csv
import math
import random
from collections import defaultdict
from dataclasses import dataclass
from pathlib import Path
from statistics import mean, pstdev
from typing import Iterable

DEFAULT_INPUT = Path("results/self_aware_astar_randomized_ablation.csv")
DEFAULT_REPORT = Path("results/self_aware_astar_statistical_report.md")

KEY_METRICS = [
    "success_rate",
    "collision_rate",
    "path_length",
    "nodes_expanded",
    "mean_risk",
    "max_risk",
    "information_gain",
    "recoverability",
    "compute_time_ms",
]


@dataclass(frozen=True)
class SummaryStats:
    mean: float
    std: float
    ci_low: float
    ci_high: float
    n: int


def read_rows(path: Path) -> list[dict[str, str]]:
    with path.open("r", encoding="utf-8", newline="") as handle:
        return list(csv.DictReader(handle))


def group_values(rows: Iterable[dict[str, str]], metric: str) -> dict[str, list[float]]:
    grouped: dict[str, list[float]] = defaultdict(list)
    for row in rows:
        method = row["method"]
        try:
            grouped[method].append(float(row[metric]))
        except (KeyError, ValueError):
            continue
    return dict(grouped)


def percentile(values: list[float], q: float) -> float:
    if not values:
        return 0.0
    ordered = sorted(values)
    idx = min(len(ordered) - 1, max(0, round((len(ordered) - 1) * q)))
    return ordered[idx]


def bootstrap_ci(values: list[float], n_bootstrap: int, confidence: float, seed: int) -> tuple[float, float]:
    if not values:
        return 0.0, 0.0
    if len(values) == 1:
        return values[0], values[0]
    rng = random.Random(seed)
    means = []
    for _ in range(n_bootstrap):
        sample = [values[rng.randrange(len(values))] for _ in values]
        means.append(mean(sample))
    alpha = (1.0 - confidence) / 2.0
    return percentile(means, alpha), percentile(means, 1.0 - alpha)


def summarize(values: list[float], n_bootstrap: int, confidence: float, seed: int) -> SummaryStats:
    if not values:
        return SummaryStats(mean=0.0, std=0.0, ci_low=0.0, ci_high=0.0, n=0)
    ci_low, ci_high = bootstrap_ci(values, n_bootstrap=n_bootstrap, confidence=confidence, seed=seed)
    return SummaryStats(
        mean=mean(values),
        std=pstdev(values) if len(values) > 1 else 0.0,
        ci_low=ci_low,
        ci_high=ci_high,
        n=len(values),
    )


def cohens_d(reference: list[float], candidate: list[float]) -> float:
    """Return Cohen's d for candidate - reference."""
    if len(reference) < 2 or len(candidate) < 2:
        return 0.0
    ref_std = pstdev(reference)
    cand_std = pstdev(candidate)
    pooled = math.sqrt((ref_std**2 + cand_std**2) / 2.0)
    if pooled == 0.0:
        return 0.0
    return (mean(candidate) - mean(reference)) / pooled


def metric_direction(metric: str) -> str:
    """Return whether lower or higher values are generally better."""
    if metric in {"success_rate", "information_gain", "recoverability"}:
        return "higher"
    return "lower"


def build_report(
    rows: list[dict[str, str]],
    n_bootstrap: int,
    confidence: float,
    baseline: str,
    seed: int,
) -> str:
    methods = sorted({row["method"] for row in rows})
    lines = [
        "# SelfAwareAStar Statistical Evaluation",
        "",
        f"Rows analysed: {len(rows)}",
        f"Bootstrap samples per confidence interval: {n_bootstrap}",
        f"Confidence level: {confidence:.0%}",
        f"Baseline for effect size: `{baseline}`",
        "",
        "## Summary tables",
        "",
    ]

    for metric in KEY_METRICS:
        grouped = group_values(rows, metric)
        if not grouped:
            continue
        direction = metric_direction(metric)
        lines.extend(
            [
                f"### `{metric}`",
                "",
                f"Preferred direction: **{direction}**.",
                "",
                "| Method | n | Mean | Std | CI low | CI high | Cohen's d vs baseline |",
                "|---|---:|---:|---:|---:|---:|---:|",
            ]
        )
        baseline_values = grouped.get(baseline, [])
        for method in methods:
            values = grouped.get(method, [])
            stats = summarize(values, n_bootstrap=n_bootstrap, confidence=confidence, seed=seed + hash(metric + method) % 10000)
            effect = cohens_d(baseline_values, values) if method != baseline else 0.0
            lines.append(
                f"| {method} | {stats.n} | {stats.mean:.4f} | {stats.std:.4f} | "
                f"{stats.ci_low:.4f} | {stats.ci_high:.4f} | {effect:.4f} |"
            )
        lines.append("")

    lines.extend(
        [
            "## Interpretation guide",
            "",
            "- Confidence intervals are bootstrap intervals over randomized map seeds.",
            "- Cohen's d is computed as method mean minus baseline mean divided by pooled standard deviation.",
            "- Positive effect size is good only for metrics where higher is better; for risk, collision, path length, and compute time, negative values usually indicate improvement.",
            "- These statistics strengthen the synthetic benchmark but still do not prove real-robot performance.",
            "",
            "## Current limitation",
            "",
            "The analysis assumes independent randomized seeds and uses a high-risk-cell collision proxy. Future work should add real simulation rollouts, dynamic obstacles, and ROS/Nav2 execution traces.",
            "",
        ]
    )
    return "\n".join(lines)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--input", type=Path, default=DEFAULT_INPUT, help="Input CSV from randomized ablation benchmark.")
    parser.add_argument("--report", type=Path, default=DEFAULT_REPORT, help="Markdown report output path.")
    parser.add_argument("--bootstrap", type=int, default=1000, help="Number of bootstrap resamples.")
    parser.add_argument("--confidence", type=float, default=0.95, help="Confidence interval level.")
    parser.add_argument("--baseline", default="classic_astar", help="Baseline method for effect sizes.")
    parser.add_argument("--seed", type=int, default=123, help="Bootstrap random seed.")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    rows = read_rows(args.input)
    report = build_report(
        rows,
        n_bootstrap=args.bootstrap,
        confidence=args.confidence,
        baseline=args.baseline,
        seed=args.seed,
    )
    args.report.parent.mkdir(parents=True, exist_ok=True)
    args.report.write_text(report, encoding="utf-8")
    print(f"Wrote {args.report}")


if __name__ == "__main__":
    main()
